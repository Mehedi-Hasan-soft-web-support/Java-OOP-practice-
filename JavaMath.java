public class JavaMath
 {
public static void main(String[] args) {
double sqrtValue = Math.sqrt(16);
System.out.println("Square root of 16: " + sqrtValue);
double powerValue = Math.pow(2, 3);
System.out.println("2 raised to the power of 3: " + powerValue);
double roundedValue = Math.round(3.6);
System.out.println("Rounded value of 3.6: " + roundedValue);
double ceilingValue = Math.ceil(3.2);
System.out.println("Ceiling value of 3.2: " + ceilingValue);
double floorValue = Math.floor(3.8);
System.out.println("Floor value of 3.8: " + floorValue);
double maxValue = Math.max(10, 20);
System.out.println("Maximum of 10 and 20: " + maxValue);
double minValue = Math.min(10, 20);
System.out.println("Minimum of 10 and 20: " + minValue);
}
}
